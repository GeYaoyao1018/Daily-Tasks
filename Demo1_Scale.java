class Demo1_Scale {	                      //scale 二进制
	public static void main(String[] args) {
		/*
		* 0b100
		* 0100
		* 0x100
		*/
		System.out.println(0b100);            //二进制
		System.out.println(0100);             //八进制
        System.out.println(100);
		System.out.println(0x100);            //16进制
	}
}
