class  Demo1_Constant {	           //constant常量
	public static void main(String[] args) {
	/*
	字符串常量   用双引号括号括起来的内容
	整数常量     所有整数
	小数常量      所有小数
	字符常量      用单引号括起来的内容 里面只能放单个的数字,字母或符号,也不能为空
	布尔常量      较为特殊 只有true和false
	空常量        null
	*/
	System.out.println("abc");  //字符串常量
    System.out.println(123);  //整数常量
    System.out.println(12.3);  //小数常量
	System.out.println('1');   //字符常量
    System.out.println(true); //布尔常量
    System.out.println(false); //布尔常量
	}
}
